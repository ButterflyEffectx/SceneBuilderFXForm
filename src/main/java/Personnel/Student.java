package Personnel;

public class Student {
    private String id;
    private String name;
    private int score;

    public void setID(String idNo) {
        this.id = idNo;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setScore(int i) {
        this.score = i;
    }

    public String getID() {
        return id;
    }

    public String getName() {
        return name;
    }

    public int getScore() {
        return score;
    }
}


