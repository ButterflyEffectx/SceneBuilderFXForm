package Personnel;

public class Person {
    private String id;
    private String name;

    public String showInfo(){ return null; }

    public void setID(String idNo) {
        this.id = idNo;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getID() {
        return id;
    }

    public String getName() {
        return name;
    }
}
